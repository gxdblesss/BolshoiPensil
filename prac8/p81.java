package prac8;

/*15. Задание Цифры числа справа налево
Дано натуральное число N. Выведите все его цифры по одной, в обратном
порядке, разделяя их пробелами или новыми строками.
При решении этой задачи нельзя использовать строки, списки, массивы (ну
                                                                             и циклы, разумеется). Разрешена только рекурсия и целочисленная арифметика.*/


public class p81 {

    // Рекурсивный метод для вывода цифр числа в обратном порядке
    public static void printDigitsReverse(int n) {
        if (n == 0) {
            return; // Базовый случай: если число равно 0, прекращаем рекурсию
        }

        // Выводим последнюю цифру числа
        System.out.print(n % 10 + " ");

        // Рекурсивно обрабатываем оставшуюся часть числа
        printDigitsReverse(n / 10);
    }

    public static void main(String[] args) {
        int n = 1234; // Пример числа

        // Если число 0, то его нужно вывести отдельно
        if (n == 0) {
            System.out.println(0);
        } else {
            printDigitsReverse(n); // Вызываем рекурсивную функцию
        }
    }
}

