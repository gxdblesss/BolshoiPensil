package prac8;

/*17. Задание Максимум последовательности
        Дана последовательность натуральных чисел (одно число в строке),
        завершающаяся числом 0. Определите наибольшее значение числа в этой
        последовательности.
        В этой задаче нельзя использовать глобальные переменные и передавать
        какие-либо параметры в рекурсивную функцию. Функция получает данные,
        считывая их с клавиатуры. Функция возвращает единственное значение:
        максимум считанной последовательности. Гарантируется, что
        последовательность содержит хотя бы одно число (кроме нуля).*/

import java.util.Scanner;

public class p83 {

    // Рекурсивная функция для поиска максимума в последовательности
    public static int findMax() {
        Scanner scanner = new Scanner(System.in);
        int num = scanner.nextInt();  // Считываем текущее число

        // Базовый случай: если введён 0, возвращаем 0
        if (num == 0) {
            return 0;
        }

        // Рекурсивный вызов для следующего числа
        int nextMax = findMax();

        // Возвращаем максимальное значение между текущим числом и найденным следующим максимумом
        return Math.max(num, nextMax);
    }

    public static void main(String[] args) {
        System.out.println("Введите последовательность чисел, заканчивающуюся нулём:");
        int max = findMax();
        System.out.println("Максимальное число в последовательности: " + max);
    }
}